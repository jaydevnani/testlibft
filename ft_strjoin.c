#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	i;
	size_t	j;
	size_t	s1len;
	size_t	s2len;

	if (!s1 || !s2)
		return (NULL);
	s1len = ft_strlen(s1);
	s2len = ft_strlen(s2);
	str = malloc(s1len + s2len + 1);
	if (!str)
		return (NULL);
	i = 0;
	j = 0;
	while (i < s1len)
	{
		str[i] = s1[i];
		i++;
	}
	while (j < s2len)
		str[i++] = s2[j++];
	str[i] = '\0';
	return (str);
}
